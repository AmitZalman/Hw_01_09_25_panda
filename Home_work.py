import numpy as np
from IPython.display import display
import pandas as pd



data = {
    "Name": ["Alice", "Bob", "Charlie", "Diana", "Evan"],
    "Age": [25, 30, 35, 28, 40],
    "Salary": [50000, 60000, 75000, 62000, 80000],
    "Department": ["HR", "IT", "Finance", "IT", "HR"]
}

df = pd.DataFrame(data)
print(df)


#QUESTION 1
print("Get the first 3 rows and only the Name and Salary columns:\n") 
display(df.loc[0:2, ['Name', 'Salary']])

#QUESTION 2
print("Create a column Bonus that is 10% of the Salary\n")
df['Bonus'] = df['Salary'] * 0.1
display(df)

#QUESTION 3(1)
print("Set the Name column as the new index:\n")
df = df.set_index('Name')

#QUESTION 3(2)
print("Display all rows from Bob to Diana and only Salary and Department columns:")
display(df.loc['Bob':'Diana',['Salary','Department']])

#QUESTION 3(3)
df = df.reset_index()
display(df)
